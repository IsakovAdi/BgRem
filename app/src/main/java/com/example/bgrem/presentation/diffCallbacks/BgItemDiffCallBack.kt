package com.example.bgrem.presentation.diffCallbacks

import androidx.recyclerview.widget.DiffUtil
import com.example.bgrem.domain.models.BackgroundResponse

class BgItemDiffCallBack(
    private val oldList: List<BackgroundResponse>,
    private val newList: List<BackgroundResponse>
) : DiffUtil.Callback() {
    override fun getOldListSize(): Int {
        return oldList.size
    }

    override fun getNewListSize(): Int {
        return newList.size
    }

    override fun areItemsTheSame(oldItemPosition: Int, newItemPosition: Int): Boolean {
        val oldItem = oldList[oldItemPosition]
        val newItem = newList[newItemPosition]
        return oldItem.id == newItem.id
    }

    override fun areContentsTheSame(oldItemPosition: Int, newItemPosition: Int): Boolean {
        val oldItem = oldList[oldItemPosition]
        val newItem = newList[newItemPosition]
        return oldItem == newItem
    }

}